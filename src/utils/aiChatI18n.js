function interpolate(template, vars = {}) {
    return String(template || "").replace(/\{\{(\w+)\}\}/g, (_, key) => (
        Object.prototype.hasOwnProperty.call(vars, key) ? String(vars[key]) : ""
    ));
}

export function normalizeAiChatUiLanguage(value) {
    const normalized = String(value || "").trim().toLowerCase();
    if (!normalized) return "en";
    if (normalized.startsWith("uk")) return "uk";
    if (normalized.startsWith("en")) return "en";
    return "en";
}

export function detectPreferredAiChatUiLanguage() {
    if (typeof navigator === "undefined") return "en";
    const candidates = [...(navigator.languages || []), navigator.language].filter(Boolean);
    const match = candidates.find((value) => /^uk\b/i.test(String(value)));
    return match ? "uk" : "en";
}

export function getAiChatText(locale, key, vars = {}) {
    const lang = normalizeAiChatUiLanguage(locale);
    const template = STRINGS[lang]?.[key] ?? STRINGS.en[key] ?? key;
    return interpolate(template, vars);
}

export function getAiChatRoleLabel(locale, role) {
    return getAiChatText(locale, role === "assistant" ? "replyingToAssistant" : "replyingToUser");
}

export function getAiChatSourceTypeLabel(locale, type = "") {
    switch (type) {
        case "docs":
            return getAiChatText(locale, "sourceDocs");
        case "schema":
            return getAiChatText(locale, "sourceSchema");
        case "config":
            return getAiChatText(locale, "sourceConfig");
        case "code":
            return getAiChatText(locale, "sourceCode");
        default:
            return getAiChatText(locale, "sourceOther");
    }
}

export function getAiChatUiLanguageOptions(locale = "en") {
    return [
        { value: "en", label: getAiChatText(locale, "uiLanguageEnglish") },
        { value: "uk", label: getAiChatText(locale, "uiLanguageUkrainian") },
    ];
}
